export const normalizeMapName = (name = '') =>
  name
    .toLowerCase()
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()

export const slugifyMapName = (name = '') =>
  normalizeMapName(name)
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')

const toMap = (name, category, index) => ({
  id: `${category.toLowerCase().replace(/\s+/g, '-')}-${slugifyMapName(name) || index}`,
  name,
  category,
  source: category === 'Original' ? 'Settlers 10th Anniversary' : 'Community Map Pack',
  createdBySeed: true,
  sortName: normalizeMapName(name),
})

export const DEFAULT_MAPS = [
  ...CORE_MAP_NAMES.map((name, index) => toMap(name, 'Original', index)),
  ...COMMUNITY_MAP_NAMES.map((name, index) => toMap(name, 'Community Pack', index)),
]

